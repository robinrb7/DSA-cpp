/*
The structure of linked list is the following

struct Node {
  int data;
  struct Node *next;
  Node(int x) {
    data = x;
    next = NULL;
  }
};
*/

class Solution {
  public:
    Node *removeDuplicates(Node *head) {
        if(!head || !head->next) return head;
        
        Node* temp = head;
        unordered_map<int,bool> umap;
        while(temp){
            umap[temp->data] =0;
            temp=temp->next;
        }
        
        Node* curr=head;
        Node* prev=NULL;
        Node* nodeToDel=NULL;
        while(curr){
            if(umap[curr->data]){
                nodeToDel = curr;
                prev->next = curr->next;
                curr= curr->next;
                delete nodeToDel;
            }
            else{
                umap[curr->data]=1;
                prev= curr;
                curr=curr->next;
            }
        }
        
        return head;
    }
};



